// Command gen renders docs/cluster-yaml.md from the embedded JSON Schema, so the
// user-facing cluster.yaml reference can never drift from what the loader accepts
// (P3.1). Run via `go generate ./internal/config`; CI asserts the output is clean.
package main

import (
	"encoding/json"
	"fmt"
	"os"
	"sort"
	"strings"
)

func main() {
	raw, err := os.ReadFile("schema.json")
	if err != nil {
		fmt.Fprintln(os.Stderr, "gen: read schema.json:", err)
		os.Exit(1)
	}
	var doc map[string]any
	if err := json.Unmarshal(raw, &doc); err != nil {
		fmt.Fprintln(os.Stderr, "gen: parse schema:", err)
		os.Exit(1)
	}

	var b strings.Builder
	b.WriteString("<!-- GENERATED by `go generate ./internal/config` from internal/config/schema.json — DO NOT EDIT. -->\n")
	b.WriteString("# cluster.yaml reference\n\n")
	if d, _ := doc["description"].(string); d != "" {
		b.WriteString(d + "\n\n")
	}
	b.WriteString("Validate a file with `pandion validate -f cluster.yaml`; scaffold a starter with `pandion init --cluster`.\n\n")

	// top-level object
	writeObject(&b, doc, doc, "Top-level fields")

	// each $def, sorted, as its own section.
	defs, _ := doc["$defs"].(map[string]any)
	names := make([]string, 0, len(defs))
	for k := range defs {
		names = append(names, k)
	}
	sort.Strings(names)
	for _, n := range names {
		if obj, ok := defs[n].(map[string]any); ok && obj["properties"] != nil {
			writeObject(&b, doc, obj, "`"+n+"`")
		}
	}

	if err := os.WriteFile("../../docs/cluster-yaml.md", []byte(b.String()), 0o644); err != nil {
		fmt.Fprintln(os.Stderr, "gen: write doc:", err)
		os.Exit(1)
	}
	fmt.Println("wrote docs/cluster-yaml.md")
}

// writeObject emits a markdown table for one object schema's properties.
func writeObject(b *strings.Builder, root, obj map[string]any, title string) {
	props, ok := obj["properties"].(map[string]any)
	if !ok {
		return
	}
	required := map[string]bool{}
	if req, ok := obj["required"].([]any); ok {
		for _, r := range req {
			if s, ok := r.(string); ok {
				required[s] = true
			}
		}
	}
	keys := make([]string, 0, len(props))
	for k := range props {
		keys = append(keys, k)
	}
	sort.Strings(keys)

	fmt.Fprintf(b, "## %s\n\n", title)
	b.WriteString("| field | type | required | default | description |\n")
	b.WriteString("|---|---|---|---|---|\n")
	for _, k := range keys {
		p, _ := props[k].(map[string]any)
		fmt.Fprintf(b, "| `%s` | %s | %s | %s | %s |\n",
			k, typeOf(root, p), yesNo(required[k]), defaultOf(p), descOf(p))
	}
	b.WriteString("\n")

	// recurse into INLINE nested objects (and arrays of inline objects) so fields
	// like provider.private_network.cidr and firewall.extra_public_ingress[].from
	// are documented too. $ref'd objects are covered by their own $def section.
	for _, k := range keys {
		p, _ := props[k].(map[string]any)
		if inline := inlineObject(p); inline != nil {
			writeObject(b, root, inline, title+" → `"+k+"`")
		}
	}
}

// inlineObject returns the inline object schema of p — either p itself when it's an
// object with properties, or its array items — but only when there is no $ref (a
// $ref is documented by its own $def section). nil otherwise.
func inlineObject(p map[string]any) map[string]any {
	if _, hasRef := p["$ref"]; hasRef {
		return nil
	}
	if _, ok := p["properties"].(map[string]any); ok {
		return p
	}
	if items, ok := p["items"].(map[string]any); ok {
		if _, hasRef := items["$ref"]; hasRef {
			return nil
		}
		if _, ok := items["properties"].(map[string]any); ok {
			return items
		}
	}
	return nil
}

func typeOf(root, p map[string]any) string {
	if ref, ok := p["$ref"].(string); ok {
		name := ref[strings.LastIndexByte(ref, '/')+1:]
		return "[`" + name + "`](#" + strings.ToLower(name) + ")"
	}
	if c, ok := p["const"]; ok {
		return fmt.Sprintf("const `%v`", c)
	}
	if enum, ok := p["enum"].([]any); ok {
		var parts []string
		for _, e := range enum {
			parts = append(parts, fmt.Sprintf("`%v`", e))
		}
		return strings.Join(parts, " \\| ")
	}
	t, _ := p["type"].(string)
	if t == "array" {
		if items, ok := p["items"].(map[string]any); ok {
			return typeOf(root, items) + "[]"
		}
		return "array"
	}
	if t == "" {
		return "—"
	}
	return t
}

func defaultOf(p map[string]any) string {
	if d, ok := p["default"]; ok {
		return fmt.Sprintf("`%v`", d)
	}
	if pat, ok := p["pattern"].(string); ok {
		return "pattern `" + pat + "`"
	}
	return "—"
}

func descOf(p map[string]any) string {
	d, _ := p["description"].(string)
	if d == "" {
		return "—"
	}
	return strings.ReplaceAll(d, "|", "\\|")
}

func yesNo(b bool) string {
	if b {
		return "yes"
	}
	return "no"
}
